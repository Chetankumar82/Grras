let week_num = 1;


// if_else ladder
if(week_num == 1) {
    console.log("Sun")
} else if(week_num == 2) {
    console.log("Mon")
} else if(week_num == 3) {
    console.log("Tues")
} else if(week_num == 4) {
    console.log("Wed")
} else if(week_num == 5) {
    console.log("Thurs")
} else if(week_num == 6) {
    console.log("Fri")
} else if(week_num == 7) {
    console.log("Sat")
} else {
    console.log("Invalid")
}